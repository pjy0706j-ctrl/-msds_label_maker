def get_section(code: str) -> str:
    num = code.replace("P", "").split("+")[0]
    if num.startswith("2"):
        return "예방"
    elif num.startswith("3"):
        return "대응"
    elif num.startswith("4"):
        return "저장"
    elif num.startswith("5"):
        return "폐기"
    return "기타"


def get_precautionary_statements(p_codes: list, max_per_section: int = 2) -> str:
    """
    P코드 리스트를 받아서 섹션별 예방조치문구 반환
    """
    sections = {"예방": [], "대응": [], "저장": [], "폐기": []}
    seen = set()

    for code in p_codes:
        code = code.strip().upper()

        if code in seen:
            continue
        seen.add(code)

        text = PCODE_DB.get(code)
        if not text:
            continue

        section = get_section(code)
        if section in sections:
            if len(sections[section]) < max_per_section:
                sections[section].append(f"{code} {text}")

    output = ""
    for section, items in sections.items():
        if items:
            output += f"<{section}>\n"
            output += "\n".join(items)
            output += "\n"

    return output.strip()
